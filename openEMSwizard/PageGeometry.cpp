void PageGeometry::SaveToSimScriptBuffer(void)
{
    text_save_to_simscript.clear();
    text_save_to_simscript.append("##<OUTPUT_AUTOGENERATED_PageGeometry>##\n");

    for(int i_sh = 0; i_sh < shapes_param_list_ptr->count(); ++i_sh)
    {
        if(shapes_param_list_ptr->at(i_sh)->type == "box")
        {
            shape_box_parameters *shape_box_tmp = (shape_box_parameters *)(shapes_param_list_ptr->at(i_sh));
            text_save_to_simscript.append(QString("CSX = AddBox(CSX, '%1', %2, ").arg(shape_box_tmp->material).arg(shape_box_tmp->priority));
            text_save_to_simscript.append(QString("[%1 %2 %3] ").arg(EvaluateVar(shape_box_tmp->x_coord_1)).arg(shape_box_tmp->y_coord_1).arg(shape_box_tmp->z_coord_1));
            text_save_to_simscript.append(QString("[%1 %2 %3]").arg(shape_box_tmp->x_coord_2).arg(shape_box_tmp->y_coord_2).arg(shape_box_tmp->z_coord_2));

            if(shape_box_tmp->transf_order[0] == "Scale" || shape_box_tmp->transf_order[0] == "Rotate" || shape_box_tmp->transf_order[0] == "Move")
            {
                text_save_to_simscript.append(QString(", 'Transform',{"));
                for(int i_tr = 0; shape_box_tmp->transf_order[i_tr] != "" && i_tr < 3; ++i_tr)
                {
                    if(shape_box_tmp->transf_order[i_tr] == "Scale")
                        text_save_to_simscript.append(QString("'Scale', '%1,%2,%3', ").arg(shape_box_tmp->transf_scale_x).arg(shape_box_tmp->transf_scale_y).arg(shape_box_tmp->transf_scale_z));
                    if(shape_box_tmp->transf_order[i_tr] == "Rotate")
                        text_save_to_simscript.append(QString("'Rotate', '%1, %2, %3, %4', ").arg(shape_box_tmp->transf_rotate_ax).arg(shape_box_tmp->transf_rotate_ay).arg(shape_box_tmp->transf_rotate_az).arg(shape_box_tmp->transf_rotate_angle));
                    if(shape_box_tmp->transf_order[i_tr] == "Move")
                        text_save_to_simscript.append(QString("'Translate', '%1,%2,%3', ").arg(shape_box_tmp->transf_move_x).arg(shape_box_tmp->transf_move_y).arg(shape_box_tmp->transf_move_z));
                }
                text_save_to_simscript.chop(2); //cut the last space and comma
                text_save_to_simscript.append(QString("}"));
            }
            text_save_to_simscript.append(QString(");\n"));
        }
        else if(shapes_param_list_ptr->at(i_sh)->type == "cylinder")
        {
            shape_cylinder_parameters *shape_cylinder_tmp = (shape_cylinder_parameters *)(shapes_param_list_ptr->at(i_sh));
            text_save_to_simscript.append(QString("CSX = AddCylinder(CSX, '%1', %2, ").arg(shape_cylinder_tmp->material).arg(shape_cylinder_tmp->priority));
            text_save_to_simscript.append(QString("[%1 %2 %3], ").arg(shape_cylinder_tmp->x_coord_1).arg(shape_cylinder_tmp->y_coord_1).arg(shape_cylinder_tmp->z_coord_1));
            text_save_to_simscript.append(QString("[%1 %2 %3], ").arg(shape_cylinder_tmp->x_coord_2).arg(shape_cylinder_tmp->y_coord_2).arg(shape_cylinder_tmp->z_coord_2));
            text_save_to_simscript.append(QString("%1);\n").arg(shape_cylinder_tmp->radius));
        }
    }

    text_save_to_simscript.append("##<END_OUTPUT_AUTOGENERATED_PageGeometry>##\n");
    qDebug("%s", qUtf8Printable(text_save_to_simscript));
}



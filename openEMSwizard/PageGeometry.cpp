void PageGeometry::UpdateVariableLists(void)
{
    sh_box_x_coord_1->clear();
    for(int i_expr = 0; i_expr < var_edit->GetVarTable()->rowCount(); ++i_expr)
        if((var_edit->GetVarTable()->item(i_expr, 0) != NULL) && (var_edit->GetVarTable()->item(i_expr, 1) != NULL))
            sh_box_x_coord_1->addItem(var_edit->GetVarTable()->item(i_expr,0)->text());
}


QString PageGeometry::EvaluateVar(QString var_to_eval)
{
    return var_edit->GetExprEngine()->evaluate(var_to_eval).toString();
}


void PageGeometry::OnChangeTransformOrder(void)
{
    QPushButton* buttonSender = qobject_cast<QPushButton*>(sender()); // retrieve the button you have clicked
    QString buttonText = buttonSender->text(); // retrive the text from the button clicked

    if(buttonText == "Scale")
    {
        transforms_list_widget->addItem("Scale");
        buttonSender->hide();
    }
    if(buttonText == "Rotate")
    {
        transforms_list_widget->addItem("Rotate");
        buttonSender->hide();
    }
    if(buttonText == "Move")
    {
        transforms_list_widget->addItem("Move");
        buttonSender->hide();
    }
    if(buttonText == "Remove" && transforms_list_widget->count())
    {
        button_transform_scale->show();
        button_transform_rotate->show();
        button_transform_move->show();
        transforms_list_widget->clear();

    }
}
